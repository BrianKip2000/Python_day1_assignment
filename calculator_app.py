#!/usr/bin/python3


def add(x, y):
    """Adds two numbers x and y"""
    return (x + y)

def multiply(x, y):
    """
    Multiply two numbers based on 3rd grade multiplication
    """
    return (x * y)

def subtract(x, y):
    """
    Subtracts y from x, and returns a result
    """
    return (x - y)

def divide(x, y):
    """
    Divides two numbers and returns result to the console
    """
    return (x / y)

